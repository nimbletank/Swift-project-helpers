// Example of a UIColor extension to be used when we need to create custom colors

import UIKit

extension UIColor {
	
	open class var rgb71_201_56: UIColor {
		get {
			return UIColor(red: 71/255, green: 201/255, blue: 56/255, alpha: 1.0)
		}
	}
}
